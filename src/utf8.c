struct UTF8Result
utf8_decode(const void *buffer) {
     struct UTF8Result result = { 0 };
     result.point = 0xFFFD;
     result.bytes = 1;

     int cursor = 0;
     uint32_t codepoint = 0;
     int state = 0;
     int done = 0;
     while (0 == done) {
          uint8_t byte = ((uint8_t *)buffer)[cursor];
          uint8_t type = states[byte];

          codepoint = (0 != state) ?
               ((byte & 0x3F) | (codepoint << 6)) :
               ((0xFF >> type) & byte);

          state = states[256 + state * 16 + type];

          done = (0 == state || 1 == state);
          cursor = cursor + 1;
     }

     result.point = (1 == state) ? 0xFFFD : codepoint;
     result.bytes = cursor;

     return result;
}
